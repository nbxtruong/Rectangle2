package rectangle;

public class Rectangle extends AbstractRectangle {
	public Rectangle(int height, int width) {
		super(height, width);
	}

	public int height() {
		return super.height();
	}

	public int width() {
		return super.width();
	}

	public void setHeight(int h) {
		super.setHeight(h);
	}

	public void setWidth(int l) {
		super.setWidth(l);
	}

}
